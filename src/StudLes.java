public class StudLes {
    private String name;
    private String phone;
    private int day;

    public StudLes(String name, String phone, int day) {
        this.name = name;
        this.phone = phone;
        this.day = day;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public int getDay() {
        return day;
    }

    public void setDay(int day) {
        this.day = day;
    }
}
